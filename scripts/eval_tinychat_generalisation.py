#!/usr/bin/env python3
"""Generalisation eval for TinyChat-trained models (roadmap item 1, 2026-07-21).

Three tiers, because six training probes cannot distinguish abstraction from
memorisation (Jonathan's review):
  A. train-form probes  — question templates exactly as trained
  B. unseen paraphrases — new surface forms composed ONLY of in-corpus words
                          (an out-of-vocab word would hit UNK and test nothing)
  C. answer-type confusion matrix over all probes — the direct binding measure
     (validation CE is dominated by syntax/frequent tokens and hides this)

Every probe is checked against the actual generated corpus vocabulary before
any model call; the script refuses to run a probe that would leak UNKs.

Usage (WSL):
  python3 eval_tinychat_generalisation.py --model ~/tinyllama_check/chatXX.gguf \
      [--binary ~/tinyllama_check/build/tinyllama] [--sampled] [--out report.md]
"""

import argparse
import json
import os
import re
import subprocess
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from prepare_chat_data import tinychat_rows  # noqa: E402

INTENTS = ["wellbeing", "food", "drink_offer", "day_activity", "weather",
           "hobby", "pet", "invitation"]

FOODS = ["pizza", "pasta", "rice", "soup", "salad", "eggs", "bread",
         "cheese", "apples", "fish", "chicken", "pancakes", "noodles"]
DRINKS = ["tea", "coffee", "juice", "water", "milk"]
PLACES = ["park", "beach", "market", "library", "museum", "garden", "city", "lake"]
ACTIVITIES = ["reading", "cooking", "running", "painting", "swimming",
              "gardening", "playing chess", "watching movies", "hiking"]
WEATHER = ["sunny", "rainy", "cold", "warm", "windy", "cloudy"]
FEELINGS = ["great", "very good", "happy", "fine", "wonderful",
            "tired", "a little sad", "busy", "sleepy", "not so good"]
PETS = ["dog", "cat", "bird", "rabbit"]

# ---- probes ---------------------------------------------------------------
# (tier, intent, question). Tier A = exact train template instances;
# tier B = paraphrases never generated by tinychat_rows (verified below).
PROBES = [
    ("A", "wellbeing",   "how are you ?"),
    ("A", "food",        "what do you like to eat ?"),
    ("A", "food",        "what is your favorite food ?"),
    ("A", "drink_offer", "would you like some tea ?"),
    ("A", "day_activity","where did you go today ?"),
    ("A", "day_activity","what did you do yesterday ?"),
    ("A", "weather",     "how is the weather today ?"),
    ("A", "hobby",       "what is your hobby ?"),
    ("A", "hobby",       "what do you like to do ?"),
    ("A", "pet",         "do you have a pet ?"),
    ("A", "invitation",  "do you want to go to the park with me ?"),

    ("B", "wellbeing",   "how do you feel ?"),                 # train form always has a day word
    ("B", "food",        "what food do you like ?"),           # recombination, never generated
    ("B", "food",        "what do you eat ?"),
    ("B", "drink_offer", "would you like some coffee or tea ?"),  # 'or' only in 'do you want X or Y ?'
    ("B", "day_activity","where did you go ?"),                # day word dropped
    ("B", "weather",     "what is the weather like today ?"),
    ("B", "hobby",       "what do you enjoy ?"),               # 'enjoy' seen only in answers
    ("B", "pet",         "do you have a cat ?"),               # specific pet in question
    ("B", "invitation",  "shall we go to the beach ?"),        # 'shall we visit P' x 'go to P' cross
]

# ---- answer classification ------------------------------------------------

def _any(reply, *phrases):
    return any(p in reply for p in phrases)

def classify(reply):
    """Map a generated reply to (intent, slot_ok). Signatures follow the
    generator's answer templates; slot_ok checks the filler word belongs to
    the slot list the intent draws from (semantic-slot accuracy)."""
    r = " " + reply.strip() + " "
    # invitation first: its refusal form "i am too busy" collides with the
    # wellbeing signature "i am ..."
    if _any(r, "that sounds lovely", "i would like that", "too busy", "sure ,"):
        return "invitation", True
    if _any(r, "i am ", "i feel "):
        m = re.search(r"i (?:am|feel) (.+?)(?: today| yesterday| this| last| ,| \.)", r)
        return "wellbeing", bool(m and m.group(1).strip() in FEELINGS)
    if _any(r, "i like ", "my favorite food is "):
        m = re.search(r"(?:i like|my favorite food is) ([a-z]+)", r)
        return "food", bool(m and m.group(1) in FOODS)
    if _any(r, "yes please", "no thank you"):
        return "drink_offer", bool(re.search(r"(" + "|".join(DRINKS) + r")", r)) or "no thank you" in r
    if _any(r, "i went to ", "i spent the day "):
        ok = bool(re.search(r"i went to the (" + "|".join(PLACES) + r")", r)
                  or re.search(r"i spent the day (" + "|".join(a.split()[0] for a in ACTIVITIES) + r")", r))
        return "day_activity", ok
    if _any(r, "it is very ", "it was "):
        return "weather", bool(re.search(r"(" + "|".join(WEATHER) + r")", r))
    if _any(r, "i really enjoy ", "my hobby is "):
        m = re.search(r"(?:i really enjoy|my hobby is) ([a-z]+)", r)
        return "hobby", bool(m and any(a.startswith(m.group(1)) for a in ACTIVITIES))
    if _any(r, "i have a little ", "is very sweet"):
        return "pet", bool(re.search(r"(" + "|".join(PETS) + r")", r))
    return "unrelated", False

# ---- harness --------------------------------------------------------------

def corpus_vocab(n=20000):
    words = set()
    for row in tinychat_rows(n_dialogues=n):
        words.update(row.split())
    return words

def run_model(binary, model, prompt, greedy, max_tokens=32):
    args = [binary, model, model, "4", "prompt", prompt,
            "--max-tokens", str(max_tokens), "--n-gpu-layers", "0"]
    if greedy:
        args += ["--top-k", "1"]
    else:
        args += ["--temperature", "0.7"]
    out = subprocess.run(args, capture_output=True, text=True, timeout=300).stdout
    reply = out.strip().split("user:")[0].strip()
    return reply

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--model", required=True)
    ap.add_argument("--binary", default=os.path.expanduser("~/tinyllama_check/build/tinyllama"))
    ap.add_argument("--sampled", action="store_true", help="add a temperature-0.7 pass")
    ap.add_argument("--out", default=None)
    args = ap.parse_args()

    vocab = corpus_vocab()
    for tier, intent, q in PROBES:
        oov = [w for w in q.split() if w not in vocab]
        if oov:
            sys.exit(f"probe would leak UNKs, fix it: [{tier}/{intent}] {q!r} -> {oov}")

    passes = [("greedy", True)] + ([("sampled@0.7", False)] if args.sampled else [])
    lines = [f"# TinyChat generalisation report — {os.path.basename(args.model)}", ""]
    results = {}
    for pname, greedy in passes:
        confusion = {}
        tier_stats = {"A": [0, 0, 0], "B": [0, 0, 0]}  # [n, intent_ok, slot_ok]
        lines.append(f"## pass: {pname}\n")
        for tier, intent, q in PROBES:
            reply = run_model(args.binary, args.model, f"user: {q} assistant:", greedy)
            pred, slot_ok = classify(reply)
            confusion[(intent, pred)] = confusion.get((intent, pred), 0) + 1
            tier_stats[tier][0] += 1
            tier_stats[tier][1] += int(pred == intent)
            tier_stats[tier][2] += int(pred == intent and slot_ok)
            mark = "OK " if pred == intent else ("~  " if pred != "unrelated" else "MISS")
            lines.append(f"- [{tier}] {mark} {intent:<12} q: {q!r}")
            lines.append(f"        -> {reply!r}  (classified: {pred}, slot_ok={slot_ok})")
        for tier in ("A", "B"):
            n, i_ok, s_ok = tier_stats[tier]
            label = "train-form" if tier == "A" else "paraphrase"
            lines.append(f"\n**Tier {tier} ({label}): answer-type {i_ok}/{n}, semantic-slot {s_ok}/{n}**")
        lines.append("\n### Confusion (true -> predicted)\n")
        for (t, p), c in sorted(confusion.items()):
            if t != p:
                lines.append(f"- {t} -> {p}: {c}")
        lines.append("")
        results[pname] = tier_stats

    report = "\n".join(lines)
    print(report)
    if args.out:
        with open(args.out, "w") as f:
            f.write(report + "\n")
        with open(args.out.replace(".md", ".json"), "w") as f:
            json.dump({p: s for p, s in results.items()}, f, indent=2)

if __name__ == "__main__":
    main()
